create table if not exists public.issues (
  id             bigint generated by default as identity primary key,
  title          text not null,
  description    text,
  status         issue_status not null default 'backlog',
  priority       issue_priority not null default 'none',
  team           issue_team,
  is_epic        boolean not null default false,
  parent_epic_id bigint references public.issues(id) on delete set null,
  due_date       date,
  assignee_id    uuid references public.users(id) on delete set null,
  created_by     text,
  created_at     timestamptz not null default now(),
  updated_at     timestamptz not null default now()
);

-- Auto-update updated_at
create extension if not exists moddatetime schema extensions;
do $$
begin
  if not exists (
    select 1 from pg_trigger where tgname = 'issues_updated_at'
  ) then
    create trigger issues_updated_at
      before update on public.issues
      for each row
      execute function extensions.moddatetime(updated_at);
  end if;
end;
$$;

alter table public.issues enable row level security;

-- Row Level Security: users can see all issues, update only ones they created or are assigned
do $$
begin
  if not exists (
    select 1 from pg_policies where schemaname = 'public' and tablename = 'issues' and policyname = 'Users can view all issues'
  ) then
    create policy "Users can view all issues" on public.issues for select using (auth.role() = 'authenticated');
  end if;
end;
$$;
do $$
begin
  if not exists (
    select 1 from pg_policies where schemaname = 'public' and tablename = 'issues' and policyname = 'Users can create issues'
  ) then
    drop policy if exists "Users can create issues" on public.issues;
    create policy "Users can create issues" on public.issues for insert with check (auth.role() = 'authenticated');
  end if;
end;
$$;
do $$
begin
  if not exists (
    select 1 from pg_policies where schemaname = 'public' and tablename = 'issues' and policyname = 'Users can update issues'
  ) then
    drop policy if exists "Users can update their own issues or assigned issues" on public.issues;
    create policy "Users can update issues" on public.issues for update using (auth.role() = 'authenticated');
  end if;
end;
$$;
do $$
begin
  if not exists (
    select 1 from pg_policies where schemaname = 'public' and tablename = 'issues' and policyname = 'Users can delete issues'
  ) then
    drop policy if exists "Users can delete their own issues" on public.issues;
    create policy "Users can delete issues" on public.issues for delete using (auth.role() = 'authenticated');
  end if;
end;
$$;

